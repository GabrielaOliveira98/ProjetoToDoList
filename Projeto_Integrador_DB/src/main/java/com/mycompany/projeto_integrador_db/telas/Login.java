    public static boolean usuarioExistente(String novaString, JComboBox<String> comboBox) {
        for (int i = 0; i < comboBox.getItemCount(); i++) {    
            if (comboBox.getItemAt(i).equals(novaString)) {
                return false;  
            }
        }
        return true;  
    }
    
    
