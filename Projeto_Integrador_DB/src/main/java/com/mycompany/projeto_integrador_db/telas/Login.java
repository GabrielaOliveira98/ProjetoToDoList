    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
      
        if (!novousuarionome.getText().equalsIgnoreCase("") && usuarioExistente(novousuarionome.getText(),usuarioantigo) == true){
        String usernovo;
        usernovo = novousuarionome.getText();
        
        Usuario newuser = new Usuario(0,usernovo); 
      
        UsuarioJPA.cadastrarUsuario(newuser);
        
        usuarioantigo.addItem(novousuarionome.getText());
        
        TelarRegistroTarefa telaregistro = new TelarRegistroTarefa(newuser);
        telaregistro.setVisible(true);
        
        novousuarionome.setText("");
        
        System.out.println(newuser);
        
        JOptionPane.showMessageDialog(null,"Bem Vindo a lista de tarefas " + usernovo + "! \n"
                + "Clique em - Única - para criar tarefas que tenham apenas um objetivo!\n"
                + "Clique em - Progressiva - para criar tarefas que sejam melhores executadas em passos menores!\n"
                + "Quando terminar sua tarefa ou algum passo especifico, selecione a tarefa na lista e clique no botão - Concluir- !\n"
                + "Lembre-se que nós disponibilizamos uma lojinha tambem! Vai lá conferir :) !");
        
        }else if (usuarioExistente(novousuarionome.getText(),usuarioantigo) == false){
            JOptionPane.showMessageDialog(null, "Usuário já existe!");
        }else{
                    
            String userlogadoNome = (String) usuarioantigo.getSelectedItem();
            
            Usuario userlogado = UsuarioJPA.buscarUsuarioPorNome(userlogadoNome);
            
            TelarRegistroTarefa telaregistro = new TelarRegistroTarefa(userlogado);
            telaregistro.setVisible(true);
    }

    }//GEN-LAST:event_entrarActionPerformed

